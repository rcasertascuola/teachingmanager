                        <input type="hidden" name="id" value="<?php echo $uda->id; ?>">
                    <?php endif; ?>

                    <div class="mb-3">
                        <label for="name" class="form-label">Nome</label>
                        <input type="text" class="form-control" id="name" name="name" value="<?php echo htmlspecialchars($uda->name ?? ''); ?>" required>
